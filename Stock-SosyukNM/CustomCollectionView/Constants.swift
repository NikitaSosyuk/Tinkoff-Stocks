struct Constants {
    static let leftDistance: CGFloat = 20
    static let rightDistance: CGFloat = 20
    static let minimumLineSpacing: CGFloat = 10
    static let itemWidth = (UIScreen.main.bounds.width - Constants.leftDistance - Constants.rightDistance - (Constants.minimumLineSpacing / 2)) / 2
}
